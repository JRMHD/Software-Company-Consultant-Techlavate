                        <script>
                            $(document).ready(function() {
                                $('#contact-form').submit(function(e) {
                                    e.preventDefault();
                                    $('#form-message').html('');
                                    $('#loading').show();

                                    var form = $(this);
                                    var submitBtn = form.find('button[type="submit"]');
                                    var url = form.attr('action');

                                    // Disable and fade the button
                                    submitBtn.prop('disabled', true).css('opacity', '0.5').css('cursor', 'not-allowed');

                                    $.ajax({
                                        type: 'POST',
                                        url: url,
                                        data: form.serialize(),
                                        success: function(response) {
                                            $('#loading').hide();
                                            $('#form-message').css('color', 'green').html(response.success);
                                            form[0].reset();
                                            // Re-enable button
                                            submitBtn.prop('disabled', false).css('opacity', '1').css('cursor',
                                                'pointer');
                                        },
                                        error: function(xhr) {
                                            $('#loading').hide();
                                            if (xhr.status === 422) {
                                                var errors = xhr.responseJSON.errors;
                                                var errorMessages = '';
                                                $.each(errors, function(key, val) {
                                                    errorMessages += val[0] + '<br>';
                                                });
                                                $('#form-message').css('color', 'red').html(errorMessages);
                                            } else {
                                                $('#form-message').css('color', 'red').html(
                                                    'An unexpected error occurred. Please try again.');
                                            }
                                            // Re-enable button
                                            submitBtn.prop('disabled', false).css('opacity', '1').css('cursor',
                                                'pointer');
                                        }
                                    });
                                });
                            });
